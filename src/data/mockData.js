// Genera datos de precio histórico ficticios para gráficos de velas
export function generateCandlestickData(ticker, days = 365) {
  const company = MOCK_COMPANIES[ticker];
  if (!company) return [];

  const currentPrice = company.price;
  const data = [];
  const now = new Date();

  // Precio base hace N días (entre 70% y 130% del precio actual)
  let price = currentPrice * (0.7 + Math.random() * 0.3);

  for (let i = days; i >= 0; i--) {
    const date = new Date(now);
    date.setDate(date.getDate() - i);

    // Skip weekends
    if (date.getDay() === 0 || date.getDay() === 6) continue;

    const volatility = 0.02;
    const drift = (currentPrice - price) / (i + 1) * 0.3;
    const change = price * (drift / price + volatility * (Math.random() - 0.48));

    const open = price;
    const close = price + change;
    const high = Math.max(open, close) * (1 + Math.random() * 0.015);
    const low = Math.min(open, close) * (1 - Math.random() * 0.015);
    const volume = Math.floor(company.volume * (0.5 + Math.random()));

    data.push({
      time: date.toISOString().split('T')[0],
      open: +open.toFixed(2),
      high: +high.toFixed(2),
      low: +low.toFixed(2),
      close: +close.toFixed(2),
      volume,
    });

    price = close;
  }

  return data;
}

// Busca empresas por ticker o nombre
export function searchCompanies(query) {
  const q = query.toUpperCase().trim();
  return Object.values(MOCK_COMPANIES).filter(
    c => c.ticker.includes(q) || c.name.toUpperCase().includes(q)
  );
}
